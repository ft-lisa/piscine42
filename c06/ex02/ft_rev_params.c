int     main(int  argc, char* argv[])
{
        char    t;
        int     i;
        int     j;

        i = 0;
        j = argc;
        while (j > 0)
        {
                while (argv[j][i] != '\0')
                {
                        t = argv[j][i];
                        write(1, &t, 1 );
                        i++;
                }
                write(1, "\n", 2);
                j--;
        }
        return (0);
}

