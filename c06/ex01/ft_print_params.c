#include <unistd.h>


int     main(int  argc, char* argv[])
{
        char    t;
        int     i;
	int	j;

        i = 0;
	j = 1;
        while (argv[j][0] != '\0')
        {	
		while (argv[j][i] != '\0')
		{
                	t = argv[j][i];
                	write(1, &t, 1 );
                	i++;
		}
		write(1, "\n", 2);
		j++;
        }
        return (0);
}

