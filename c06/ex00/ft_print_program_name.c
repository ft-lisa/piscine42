#include <unistd.h>

int	main(int argc, char **argv)
{
	char	t;
	int		i;

	(void)argc;
	i = 0;
	while (argv[0][i] != '\0')
	{
		t = argv[0][i];
		write(1, &t, 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
