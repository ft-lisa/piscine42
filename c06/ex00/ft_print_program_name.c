#include <unistd.h>


int	main(int  argc, char* argv[])
{
	char	t;
	int	i;

	i = 0;
	while (argv[0][i] != '\0')
	{
		t = argv[0][i];
		write(1, &t, 1 );
		i++;
	}
	return (0);
}
